    private void press()
    {
         Audio.playmusic("Music\\\\Button.wav");
         try
				{
					Class.forName("com.mysql.jdbc.Driver");
					con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gym", "root" , "root");
					System.out.println("Connection Established...");	
                                        if(jPasswordField1.getText().length()==0 && jPasswordField2.getText().length()==0)
					{
						new ErrorPage_DataFieldEmpty().setVisible(true);
                                               
					}
					else
					{
                                                ps = con.prepareStatement("update customer set password ='"+jPasswordField2.getText()+"' where  username='"+LoginPage_ForgotPassword_Customer.s+"'");
                                                ps.executeUpdate(); 
                                                 if(jPasswordField1.getText().equals(jPasswordField2.getText()))
						{
							
                                                        this.setVisible(false);                                                      
                                                        new LoginPage_Customer().setVisible(true);
						}
						else
						{
                                                    Audio.playmusic("Music\\\\Error.wav");
							new ErrorPage_incorrect().setVisible(true);
						}
                                        }
                                }
        catch(Exception ex)
				{
					System.out.println("Exception : "+ex);
				}
    }
    
